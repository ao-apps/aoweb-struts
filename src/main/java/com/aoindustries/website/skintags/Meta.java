package com.aoindustries.website.skintags;

public class Meta {

	private final String name;
	private final String content;

	public Meta(String name, String content) {
		this.name = name;
		this.content = content;
	}

	public String getName() {
		return name;
	}

	public String getContent() {
		return content;
	}
}
